class Student{
    private int studentid;
    private String name;
    private int dayAttended;

    Student(){
        this.studentid=studentid;
        this.name=name;
        this.dayAttended=dayAttended;
    }

    int getStudentid(){
        return studentid;
    }
    void setStudentid(int studentid){
        this.studentid=studentid;
    }

    String getName(){
        return name;
    }
    void setName(String name){
        this.name=name;
    }

    int getDaysAttended(){
        return dayAttended;
    }
    void setDaysAttended(int daysAttended){
        this.daysAttended=daysAttened;
    }
}
